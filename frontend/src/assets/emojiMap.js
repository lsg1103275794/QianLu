export function getEmoji(type, key, fallback = '') {
  let map;
  
  switch(type) {
    case 'menu':
      map = MENU_EMOJI;
      break;
    case 'provider':
      map = PROVIDER_EMOJI;
      break;
    case 'feature':
      map = FEATURE_EMOJI;
      break;
    default:
      return fallback;
  }
  
  return map[key] || fallback;
}

// ä¸ºæ–‡æœ¬æ·»åŠ emojiå‰ç¼€
export function addEmoji(text, type, key) {
  const emoji = getEmoji(type, key, '');
  return emoji ? `${emoji} ${text}` : text;
}